package day2;

public class Conditional_Logic {
  public static void main(String[] args) {
    if (20 > 18) {
      System.out.println("20 is greater than 18");
    } else if (10 < 18) {
      System.out.println("20 is less than 18");
    } else {
      System.out.println("else statement will execute when other statements will not execute");
    }
  }
}
